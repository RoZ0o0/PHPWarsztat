    in_id_zamowienia IN zamowienia.id_zamowienia%TYPE) IS
    czesc_zam CZESCI_ZAMOWIENIA%ROWTYPE;
    CURSOR kursor1 IS Select * FROM CZESCI_ZAMOWIENIA WHERE id_zamowienia=in_id_zamowienia;    
    BEGIN
    OPEN kursor1;
    LOOP
        FETCH kursor1 INTO czesc_zam;
        EXIT WHEN kursor1%NOTFOUND;
        UPDATE CZESCI SET liczba_dostepnych_sztuk = liczba_dostepnych_sztuk+czesc_zam.liczba_zam_sztuk WHERE id_czesci = czesc_zam.id_czesci;
    END LOOP;
    CLOSE kursor1;
    
    UPDATE zamowienia SET status=1 WHERE id_zamowienia=in_id_zamowienia;
    COMMIT;
    END ZAMOWIENIA_CONFIRM;

